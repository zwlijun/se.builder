function files2group(files) {
  var group = []
  var hash = {}

  for (var i = 0, len = files.length; i < len; i++) {
    var file = files[i]
    var ext = getExt(file)
    if (ext) {
      (hash[ext] || (hash[ext] = [])).push(file)
    }
  }

  for (var k in hash) {
    if (hash.hasOwnProperty(k)) {
      group.push(hash[k])
    }
  }

  return group
}

