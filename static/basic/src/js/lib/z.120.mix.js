function meta2paths(meta) {
  var paths = []
  var __KEYS = meta.__KEYS

  for (var i = 0, len = __KEYS.length; i < len; i++) {
    var part = __KEYS[i]
    var root = part
    var m = meta[part]
    var KEYS = m.__KEYS

    while (KEYS.length === 1) {
      root += "/" + KEYS[0]
      m = m[KEYS[0]]
      KEYS = m.__KEYS
    }

    if (KEYS.length) {
      paths.push([root.replace("__", "://"), meta2arr(m)])
    }
  }

  return paths
}

