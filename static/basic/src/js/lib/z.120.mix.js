function uris2meta(uris) {
  var meta = {
    __KEYS: []
  }

  for (var i = 0, len = uris.length; i < len; i++) {
    var parts = uris[i].replace("://", "__").split("/")
    var m = meta

    for (var j = 0, l = parts.length; j < l; j++) {
      var part = parts[j]

      if (!m[part]) {
        m[part] = {
          __KEYS: []
        }
        m.__KEYS.push(part)
      }
      m = m[part]
    }
  }

  return meta
}

